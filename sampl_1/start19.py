# 11. Formatted String (f-string) - A string that allows embedding expressions inside curly braces.
name = "Anu"
formatted_string = f"Hello, {name}!"

# 12. String with Escape Characters - A string that includes special characters using backslashes.
escape_string = "He said, \"Hello!\""
 

# 13. Checking Substring Presence - Using the in keyword to check if a substring exists within a string.
contains_substring = "Hello" in "Hello, World!"  

# 14. String Replacement - Replacing occurrences of a substring with another substring using .replace().
replaced_string = "Hello, World!".replace("World", "Python")  

# 15. Stripping Whitespace - Removing leading and trailing whitespace from a string using .strip().
stripped_string = "   Hello   ".strip()  

# 16. Splitting a String - Splitting a string into a list based on a delimiter using .split().
split_string = "a,b,c".split(",")  

# 17. Joining Strings - Joining elements of a list into a single string with a specified separator using .join().
joined_string = ", ".join(["a", "b", "c"])  

# 18. Checking String Start/End - Checking if the string starts with or ends with a specific substring.
starts_with_hello = "Hello, World!".startswith("Hello")  
ends_with_world = "Hello, World!".endswith("World!")    

# 19. Sliced String - Extracting a substring from a string using slicing syntax.
sliced_string = "Hello"[1:4]   